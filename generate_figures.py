# FIGURE S1: Metric coefficient continuity across the boundary
# ══════════════════════════════════════════════════════════════════════

def fig_metric_continuity():
    """
    All three independent metric coefficients (g_TT, |g_Tr|, g_rr) as
    functions of areal radius r, evaluated across the dust surface at
    R = 5M.  Both interior and exterior are written in (T, r) coordinates
    with N = 1.

    Interior (FRW dust, PG-like coords):
        g_TT = 1 - H^2 r^2,   |g_Tr| = Hr,   g_rr = +1
        where H = sqrt(2M / R^3)

    Exterior (PG):
        g_TT = 1 - 2M/r,   |g_Tr| = sqrt(2M/r),   g_rr = +1
    """
    fig, axes = plt.subplots(1, 3, figsize=(12, 4))

    R_surface = 5.0 * M
    H = np.sqrt(2 * M / R_surface**3)

    r_int = np.linspace(0.01, R_surface, 200)
    r_ext = np.linspace(R_surface, 12 * M, 200)

    # ── Panel (a): g_{TT} ──
    gTT_int = 1 - (H * r_int)**2
    gTT_ext = 1 - 2 * M / r_ext

    axes[0].plot(r_int / M, gTT_int, 'b-', lw=2, label='Interior')
    axes[0].plot(r_ext / M, gTT_ext, 'r-', lw=2, label='Exterior')
    axes[0].axvline(R_surface / M, color='gray', ls='--', lw=0.8, alpha=0.5)
    axes[0].set_xlabel(r'$r/M$')
    axes[0].set_ylabel(r'$g_{TT}$')
    axes[0].set_title(r'(a) $g_{TT} = 1 - v^2$', fontsize=11)
    axes[0].legend(fontsize=9)

    gTT_int_surf = 1 - (H * R_surface)**2
    gTT_ext_surf = 1 - 2 * M / R_surface
    axes[0].plot(R_surface / M, gTT_int_surf, 'ko', ms=6, zorder=5)
    print(f"  g_TT continuity: int={gTT_int_surf:.6f}, ext={gTT_ext_surf:.6f}, "
          f"delta={abs(gTT_int_surf - gTT_ext_surf):.2e}")

    # ── Panel (b): |g_{Tr}| ──
    axes[1].plot(r_int / M, H * r_int, 'b-', lw=2, label='Interior')
    axes[1].plot(r_ext / M, np.sqrt(2 * M / r_ext), 'r-', lw=2, label='Exterior')
    axes[1].axvline(R_surface / M, color='gray', ls='--', lw=0.8, alpha=0.5)
    axes[1].set_xlabel(r'$r/M$')
    axes[1].set_ylabel(r'$|g_{Tr}|$')
    axes[1].set_title(r'(b) $|g_{Tr}| = |v|$', fontsize=11)
    axes[1].legend(fontsize=9)

    gTr_int_surf = H * R_surface
    gTr_ext_surf = np.sqrt(2 * M / R_surface)
    axes[1].plot(R_surface / M, gTr_int_surf, 'ko', ms=6, zorder=5)
    print(f"  g_Tr continuity: int={gTr_int_surf:.6f}, ext={gTr_ext_surf:.6f}, "
          f"delta={abs(gTr_int_surf - gTr_ext_surf):.2e}")

    # ── Panel (c): g_{rr} ──
    axes[2].plot(r_int / M, np.ones_like(r_int), 'b-', lw=2, label='Interior')
    axes[2].plot(r_ext / M, np.ones_like(r_ext), 'r-', lw=2, label='Exterior')
    axes[2].axvline(R_surface / M, color='gray', ls='--', lw=0.8, alpha=0.5)
    axes[2].set_xlabel(r'$r/M$')
    axes[2].set_ylabel(r'$g_{rr}$')
    axes[2].set_title(r'(c) $g_{rr} = +1$', fontsize=11)
    axes[2].legend(fontsize=9)
    axes[2].set_ylim(0.7, 1.3)

    fig.tight_layout(w_pad=1.0)
    fig.savefig(os.path.join(FIGDIR, 'figS1_metric_continuity.pdf'))
    fig.savefig(os.path.join(FIGDIR, 'figS1_metric_continuity.png'))
    plt.close(fig)
    print("Figure S1: Metric continuity — done.")


