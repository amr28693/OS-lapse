# ── Physical parameters (G = c = 1 units) ────────────────────────────
M = 1.0                          # Total mass of the dust ball
R0 = 10.0 * M                 # Initial areal radius of the dust surface
rs = 2.0 * M                     # Schwarzschild radius


# ══════════════════════════════════════════════════════════════════════
# FIGURE 1: Lapse function across the boundary
# ══════════════════════════════════════════════════════════════════════

def fig_lapse_comparison():
    """
    Shows N(r) for PG (N=1 everywhere) vs Schwarzschild [N = sqrt(1-2M/r)]
    evaluated at a snapshot when the dust surface is at R = 5M.
    """
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(10, 4.2), sharey=True)

    R_surface = 5.0 * M

    r_int = np.linspace(0.01, R_surface, 200)
    r_ext = np.linspace(R_surface, 15 * M, 200)

    # ── Panel (a): PG gauge ──
    ax1.plot(r_int / M, np.ones_like(r_int), 'b-', lw=2.2,
             label='Interior (FRW comoving)')
    ax1.plot(r_ext / M, np.ones_like(r_ext), 'r-', lw=2.2,
             label='Exterior (PG)')
    ax1.axvline(R_surface / M, color='gray', ls='--', lw=1, alpha=0.7)
    ax1.annotate(r'$r = R(\tau)$', xy=(R_surface / M, 0.55),
                 fontsize=10, color='gray', ha='center',
                 bbox=dict(boxstyle='round,pad=0.2', facecolor='white',
                           edgecolor='none', alpha=0.9))
    ax1.set_xlabel(r'$r / M$')
    ax1.set_ylabel(r'Lapse function $N$')
    ax1.set_title(r'(a) $N = 1$ gauge (PG exterior)', fontsize=12)
    ax1.set_ylim(0, 1.35)
    ax1.set_xlim(0, 15)
    ax1.legend(loc='upper right', framealpha=0.9)

    ax1.plot(R_surface / M, 1.0, 'ko', ms=8, zorder=5)
    ax1.annotate(r'$N_{\mathrm{int}} = N_{\mathrm{ext}} = 1$',
                 xy=(R_surface / M, 1.0), xytext=(R_surface / M + 2.5, 1.15),
                 fontsize=10, color='black',
                 arrowprops=dict(arrowstyle='->', color='black', lw=1.2))

    # ── Panel (b): Schwarzschild gauge ──
    N_ext_Sch = np.sqrt(1 - rs / r_ext)

    ax2.plot(r_int / M, np.ones_like(r_int), 'b-', lw=2.2,
             label='Interior (FRW comoving)')
    ax2.plot(r_ext / M, N_ext_Sch, 'r-', lw=2.2,
             label='Exterior (Schwarzschild)')
    ax2.axvline(R_surface / M, color='gray', ls='--', lw=1, alpha=0.7)
    ax2.annotate(r'$r = R(\tau)$', xy=(R_surface / M, 0.55),
                 fontsize=10, color='gray', ha='center',
                 bbox=dict(boxstyle='round,pad=0.2', facecolor='white',
                           edgecolor='none', alpha=0.9))
    ax2.set_xlabel(r'$r / M$')
    ax2.set_title(r'(b) Diagonal gauge (Schwarzschild exterior)', fontsize=12)
    ax2.set_xlim(0, 15)
    ax2.legend(loc='upper right', framealpha=0.9)

    N_ext_at_surface = np.sqrt(1 - rs / R_surface)
    ax2.plot(R_surface / M, 1.0, 'bo', ms=7, zorder=5)
    ax2.plot(R_surface / M, N_ext_at_surface, 'ro', ms=7, zorder=5)

    ax2.annotate('', xy=(R_surface / M + 0.15, N_ext_at_surface),
                 xytext=(R_surface / M + 0.15, 1.0),
                 arrowprops=dict(arrowstyle='<->', color='darkorange', lw=2))
    gap = 1.0 - N_ext_at_surface
    ax2.annotate(r'$\Delta N = %.3f$' % gap,
                 xy=(R_surface / M + 0.3, (1.0 + N_ext_at_surface) / 2),
                 fontsize=10, color='darkorange', ha='left', va='center')

    fig.tight_layout(w_pad=1.5)
    fig.savefig(os.path.join(FIGDIR, 'fig1_lapse_comparison.pdf'))
    fig.savefig(os.path.join(FIGDIR, 'fig1_lapse_comparison.png'))
    plt.close(fig)
    print("Figure 1: Lapse comparison — done.")


# ══════════════════════════════════════════════════════════════════════
# FIGURE 2: Velocity field / shift vector across the boundary
# ══════════════════════════════════════════════════════════════════════

def fig_velocity_field():
    """
    For marginally bound OS collapse (E=0, dust falling from rest at infinity):
      Interior: v(r,tau) = (2M/R^3)^{1/2} * r   (Hubble-like)
      Exterior: v(r) = sqrt(2M/r)                (PG velocity field)
    At the dust surface r = R(tau), both give v = sqrt(2M/R).
    """
    fig, ax = plt.subplots(figsize=(7, 4.5))

    R_values = [8.0, 5.0, 3.0, 2.5]
    colors = ['#1b9e77', '#d95f02', '#7570b3', '#e7298a']
    r_max = 12.0

    for i, R in enumerate(R_values):
        r_int = np.linspace(0, R, 150)
        v_int = -np.sqrt(2 * M / R**3) * r_int

        r_ext = np.linspace(R, r_max, 150)
        v_ext = -np.sqrt(2 * M / r_ext)

        ax.plot(r_int / M, np.abs(v_int), '-', color=colors[i], lw=2.0,
                label=r'$R(\tau) = %.1fM$' % (R / M))
        ax.plot(r_ext / M, np.abs(v_ext), '-', color=colors[i], lw=2.0)

        v_match = np.sqrt(2 * M / R)
        ax.plot(R / M, v_match, 'o', color=colors[i], ms=7, zorder=5,
                markeredgecolor='k', markeredgewidth=0.8)

    ax.axvline(rs / M, color='gray', ls=':', lw=1, alpha=0.5)
    ax.annotate(r'$r_s = 2M$', xy=(rs / M, 0.02), fontsize=9, color='gray')

    ax.set_xlabel(r'Areal radius $r / M$')
    ax.set_ylabel(r'$|v(r)|$ (velocity field magnitude)')
    ax.set_title(r'Shift vector matching at the dust surface', fontsize=12)
    ax.legend(loc='upper right', framealpha=0.9, title=r'Dust surface position')
    ax.set_xlim(0, r_max / M)
    ax.set_ylim(0, 1.15)

    ax.text(2.0, 0.95, r'Interior: $v = \sqrt{2M/R^3}\;r$',
            fontsize=10, color='#555555', ha='left',
            bbox=dict(boxstyle='round,pad=0.3', facecolor='lightyellow',
                      edgecolor='gray', alpha=0.9))
    ax.text(7.8, 0.58, r'Exterior: $v = \sqrt{2M/r}$',
            fontsize=10, color='#555555', ha='left',
            bbox=dict(boxstyle='round,pad=0.3', facecolor='lightyellow',
                      edgecolor='gray', alpha=0.9))

    fig.tight_layout()
    fig.savefig(os.path.join(FIGDIR, 'fig2_velocity_matching.pdf'))
    fig.savefig(os.path.join(FIGDIR, 'fig2_velocity_matching.png'))
    plt.close(fig)
    print("Figure 2: Velocity field matching — done.")


